# This code stores a list and print the list in each list functions
countries = ['ghana', 'usa', 'england', 'spain', 'australia']
print(countries)
# Printed sorted() function without modifying actual list
print(sorted(countries))
# Printed main list 
print(countries)
# reverse() method changing the order of the list
countries.reverse()
print(countries)
# reverse() method changing the order of the list back to the main chronological order
countries.reverse()
print(countries)
# sort() method making list store in alphabetical order
countries.sort()
print(countries)
# sort() method making list store in reverse alphabetical order
countries.sort(reverse=True)
print(countries)

