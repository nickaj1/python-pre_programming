# This code stores list of mountains and uses each functions of list to print the lists
mountains = ['Mount Everest', 'K2', 'Kilimanjaro', 'Denali', 'Matterhorn']
# sort() Method 
mountains.sort()
print(mountains)

# sort() reverse Method
mountains.sort(reverse=True)
print(mountains)

# sorted() function
print(sorted(mountains))
print(mountains)

# reverse() Method
mountains.reverse()
print(mountains)

# len() function
print(len(mountains))